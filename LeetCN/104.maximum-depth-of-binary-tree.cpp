/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    int max_d = 0;
    void check_depth(TreeNode* n, int current_dpt) {
        if (!n){ max_d = max(max_d, current_dpt);return;}
        check_depth(n->left, current_dpt+1);
        check_depth(n->right, current_dpt+1);
    }
    int maxDepth(TreeNode* root) {
        check_depth(root,0);
        return max_d;
    }
};
