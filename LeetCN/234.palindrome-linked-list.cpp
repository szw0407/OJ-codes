/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    bool isPalindrome(ListNode* head) {
        vector<int> ls;
        while(head){
            ls.push_back(head->val);
            head = head->next;
        }
        auto k = ls.size() -1;
        auto j = 0;
        bool res = true;
        while (k > j && res) {
            res = res && (ls[k] == ls[j]); 
            k--;
            j++;
        }
        return res;
    }
};
